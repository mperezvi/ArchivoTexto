    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        try {
            // TODO add your handling code here:
            String idProducto;
            limpiarTextos();
            idProducto=JOptionPane.showInputDialog("id del Producto a buscar : ");
            Producto x;
            x=ap.buscar(idProducto);
            if(x!=null)
            {
              txtIdProducto.setText(x.getCodigo());
              txtNombre.setText(x.getDescripcion());
              txtPrecio.setText(String.valueOf(x.getPrecio()));
              txtStock.setText(String.valueOf(x.getStock()));
            }
            else
                JOptionPane.showMessageDialog(null,"El codigo  del Producto no existe");
        } catch (FileNotFoundException ex) {
            Logger.getLogger(DialogProducto.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(DialogProducto.class.getName()).log(Level.SEVERE, null, ex);
        }
}//GEN-LAST:event_btnBuscarActionPerformed

