    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        // TODO add your handling code here:
        String idProducto,nombre;
        double precio;
        int stock;
        if(txtIdProducto.getText().compareTo("")!=0 && txtNombre.getText().compareTo("")!=0 && txtPrecio.getText().compareTo("")!=0
                && txtStock.getText().compareTo("")!=0 ) {
            try {
                idProducto=txtIdProducto.getText();
                nombre=txtNombre.getText();
                precio=Double.parseDouble(txtPrecio.getText());
                stock =Integer.parseInt(txtStock.getText());
                Producto prod;
                prod=new Producto(idProducto,nombre,precio,stock);
                ap.insertaProducto(prod);
                limpiarTextos();
                textos(false);
                controles(true);
            } catch (IOException ex) {
                Logger.getLogger(DialogProducto.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else
            JOptionPane.showMessageDialog(null,"faltan llenar datos");
}//GEN-LAST:event_btnRegistrarActionPerformed

